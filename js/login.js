// Seleccionamos el checkbox y el campo de la contraseña
const showPasswordCheckbox = document.getElementById('show_password');
const passwordField = document.getElementById('password');

// Agregamos un evento al checkbox
showPasswordCheckbox.addEventListener('change', function() {
  // Si el checkbox está marcado, mostramos la contraseña
  if (this.checked) {
    passwordField.type = 'text';  // Cambia el tipo de password a texto
  } else {
    passwordField.type = 'password';  // Si está desmarcado, lo volvemos a ocultar
  }
});
